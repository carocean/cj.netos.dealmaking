package cj.netos.dealmaking.args;

public class DealmakingEngineInfo {
	String market;
	String dealmakingEngineID;
	boolean isInitialized;
	public String getMarket() {
		return market;
	}
	public void setMarket(String market) {
		this.market = market;
	}
	public String getDealmakingEngineID() {
		return dealmakingEngineID;
	}
	public void setDealmakingEngineID(String dealmakingEngineID) {
		this.dealmakingEngineID = dealmakingEngineID;
	}
	public boolean isInitialized() {
		return isInitialized;
	}
	public void setInitialized(boolean isInitialized) {
		this.isInitialized = isInitialized;
	}
	
}
